  function updateItemList(filteredItems = items) {
    const suggestionsContainer = document.querySelector('.suggestions');
    suggestionsContainer.innerHTML = '';
    
    filteredItems.forEach(item => {
      const div = document.createElement('div');
      div.classList.add('suggestion-item');
      div.textContent = item.name;
      div.addEventListener('click', () => {
        document.querySelector('.search-input').value = item.name;
        suggestionsContainer.style.display = 'none';
        showItemDetails(item);
      });
      suggestionsContainer.appendChild(div);
    });
  }
  
  function filterItems(searchText) {
    if (!searchText) return items;
    
    return items.sort((a, b) => {
      const aStartsWith = a.name.toLowerCase().startsWith(searchText.toLowerCase());
      const bStartsWith = b.name.toLowerCase().startsWith(searchText.toLowerCase());
      
      if (aStartsWith && !bStartsWith) return -1;
      if (!aStartsWith && bStartsWith) return 1;
      
      const aIncludes = a.name.toLowerCase().includes(searchText.toLowerCase());
      const bIncludes = b.name.toLowerCase().includes(searchText.toLowerCase());
      
      if (aIncludes && !bIncludes) return -1;
      if (!aIncludes && bIncludes) return 1;
      
      return 0;
    }).filter(item => 
      item.name.toLowerCase().includes(searchText.toLowerCase())
    );
  }
  
